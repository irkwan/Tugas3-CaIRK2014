    public void SolveGame(Point root){
        
        if (MatrixChar[root.x][root.y].taken==true){
            
        }
        else
        {
            container=container+MatrixChar[root.x][root.y].karakter;
            MatrixChar[root.x][root.y].taken=true;
            if (Dictionary.contains(container)){
                if (ListWord.contains(container)==false){
                    ListWord.addElement(container);
                    ListScore.addElement(container.length());
                    score=score+container.length();
                }
            }
            ArrayList<Point> Neighbor=new ArrayList<>();
            Neighbor=CariTetangga(root.x,root.y);
            for (int c=0;c<Neighbor.size();c++){
                SolveGame(Neighbor.get(c));
            }
            MatrixChar[root.x][root.y].taken=false;
            container=container.substring(0, container.length()-1);
        }
        
        
    }
