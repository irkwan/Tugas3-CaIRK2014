    private char GetChar(int i,int j){
        
        String temp;
        temp=null;
        if (i==0){
            if (j==0){
                temp= jTextField1.getText();
            }
            else if(j==1){
                temp= jTextField2.getText();
            }
            else if(j==2){
                temp= jTextField3.getText();
            }
            else if(j==3){
                temp= jTextField4.getText();
            }
        }
        else
        if (i==1){
            if (j==0){
                temp= jTextField17.getText();
            }
            else if (j==1){
                temp= jTextField18.getText();
            }
            else if (j==2){
                temp= jTextField19.getText();
            }
            else if (j==3){
                temp= jTextField20.getText();
            }
        }
        else
        if (i==2){
            if (j==0){
                temp= jTextField21.getText();
            }
            else if (j==1){
                temp= jTextField22.getText();
            }
            else if (j==2){
                temp= jTextField23.getText();
            }
            else if (j==3){
                temp= jTextField24.getText();
            }
        }
        if (i==3){
            if (j==0){
                temp= jTextField25.getText();
            }
            else if (j==1){
                temp= jTextField26.getText();
            }
            else if (j==2){
                temp= jTextField27.getText();
            }
            else if (j==3){
                temp= jTextField28.getText();
            }
        }
        if ("".equals(temp)){
            return '#';
        }
        else{
            temp.trim();
            if (temp.length()>1)
            {
                return '#';
            }
            else
            if (isLetter(temp.charAt(0))==false){
                return '#';
            }
            else
            {
                return temp.charAt(0);
            }
        }
        
    }
    
    private void SolveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SolveButtonActionPerformed
        // TODO add your handling code here:
        
        //inisialisasi Component
        score=0;
        container="";
        ListWord.clear();
        ListScore.clear();
        
        boolean SalahKarakter=false;
        //inisialisasi selesai
        int i=0;
        int j=0;
        while ((i<4) && (!SalahKarakter)){
            while ((j<4) && (!SalahKarakter)){
                if (GetChar(i,j)=='#'){
                    SalahKarakter=true;
                }
                else
                {
                    MatrixChar[i][j].karakter=toLowerCase(GetChar(i,j));
                    j++;
                }
            }
            i++;
            j=0;
        }
        
        //Kasus jika ada karakter tidak valid
        if (SalahKarakter){
            JOptionPane.showMessageDialog(null,"Input harus berupa satu buah karakter, tidak boleh simbol, angka, maupun string");
        }
        else
        //Kasus semua karakter valid, mulai solve
        {
            //Mulai Pengerjaan Wordament
            for (int a=0;a<4;a++){
                for (int b=0;b<4;b++){
                    SolveGame(new Point(a,b));
                }
            }
            String temporary="Score Total: ";
            temporary=temporary+Integer.toString(score);
            ListScore.addElement(temporary);
            String tempor="Jumlah Kata: ";
            tempor=tempor+Integer.toString(ListWord.size());
            ListWord.addElement(tempor);
        }
    }//GEN-LAST:event_SolveButtonActionPerformed

